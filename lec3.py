# int - integer data type

x = 100
y = -200
z = 0

print("datatype of x is: ",type(x))
print("datatype of y is: ",type(y))
print("datatype of z is: ",type(z))

# float - floating data type
a = 3.14
b = -0.5
c = 2.0

print("datatype of a is: ",type(a))
print("datatype of b is: ",type(b))
print("datatype of c is: ",type(c))

# complex datatype
p = 2 + 3j
q = -1.5 + 0.5j
r =  0 -2j

print("datatype of p is: ",type(p))
print("datatype of q is: ",type(q))
print("datatype of r is: ",type(r))

# bool - boolean datatype

is_sunny = True
is_raining = False

if is_sunny:
    print("The day is sunny!")
else:
    print("This day is not sunny")

print("datatype of is_sunny is: ",type(is_sunny))
print("datatype of is_raining is: ",type(is_raining))

# str - string datatype
coders = "Rising Coders Era"
message = 'Like,Share,Subscribe'

print("datatype of coders is: ",type(coders))
print("datatype of message is: ",type(message))